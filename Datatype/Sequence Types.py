#Sequence Types:list, tuple, range 
#list
x=["apple","banana","cherry"]
print(type(x))
#tuple
y=("apple","banana")
print(type(y))
#range
z=range(6);
print(type(z));

#or
a=list(("apple","banana","cherry"))
print(type(a))
b=tuple(("apple","banana"))
print(type(b))
c=range(6)
print(type(c))